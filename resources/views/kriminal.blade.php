<h1>Perkosa Anak Kandung Sampai Hamil 8 Bulan, Ayah di Samarinda Ditangkap</h1>
<img src="{{ asset('img/kriminal.jpeg') }}" class="img-responsive" style="margin-left: auto;margin-right: auto; margin-top: auto;margin-bottom: auto;width:75%;" alt="Tri">
<p>Seorang gadis 14 tahun di Samarinda, Kalimantan Timur (Kaltim) menjadi korban pemerkosaan ayah kandungnya sendiri, PS (32). Gadis itu kini hamil 8 bulan.
"Hari Jumat (20/11/2020) lalu kami menerima laporan dari korban, ditemani keluarga dan ibu korban. Korban masih berusia 14 tahun dan masih pelajar," kata Kanit PPA Polresta Samarinda Iptu Teguh Wibowo pada Rabu (25/11/2020) pagi.</p>

<p>Terungkapnya kasus ini, lanjut Teguh, karena korban bercerita kepada bibinya. Cerita korban kemudian disampaikan oleh bibinya kepada ibu korban.</p>

<p>"Sebelum terungkap, ibu korban pernah menanyakan perubahan tubuh si korban. Namun oleh korban dijawab tidak ada apa-apa. Namun terakhir saat mengetahui dirinya hamil, barulah korban menceritakan kejadian pilu yang dialaminya kepada bibinya," kata Teguh.</p>

<p>"Karena takut, akhirnya kini korban hamil 8 bulan lebih ini. Karena pelaku telah melakukan perbuatannya berkali-kali," imbuh Teguh.</p>

<p>Polisi yang mendapat laporan dari korban dan ibunya langsung membekuk pelaku.</p>

<p>"Berdasarkan pemeriksaan dan alat bukti, kemudian mencari keberadaan pelaku. Kami dapatkan pelaku masih ada di rumahnya dan kami amankan ke Polresta Samarinda untuk penyidikan lebih lanjut," jelas Teguh.</p>

<p>Teguh menyampaikan korban mengaku kerap disetubuhi ayahnya saat rumah dalam kondisi sepi. Ibu korban memang kerap meninggalkan korban dan suaminya berdua di rumah karena mencari nafkah berjualan.</p>

<p>"Saat itu korban hanya berdua bersama ayah tirinya, sementara sang ibu sedang kerja, berjualan. Saat korban sedang berjalan mau mandi, ternyata dalam perjalanan korban ditarik sama pelaku dan dibawa ke dapur," terang Teguh.</p>

<p>"Pelaku pun memaksa korban untuk mau melayaninya, pelaku melampiaskan nafsu bejatnya. Korban sempat berusaha melawan namun karena tubuhnya yang kecil membuat perlawanan yang dilakukan sia sia," sambung dia.</p>

<p>Teguh mengungkapkan korban saat ini dalam kondisi syok. Korban juga mengaku sulit mengingat detail waktu kejadian pemerkosaan tersebut.</p>

<p>Kepada polisi, korban mengingat-ingat pemerkosaan yang dialaminya terjadi tiga kali yakni pada 2019, dan Maret serta September 2020.</p>

<p>"Kami masih menggali apakah cerita dari ibunya ini sudah apa adanya atau ada apanya. Karena bagaimanapun juga hubungan mereka masih harmonis. Ibu keluar rumah karena kerja, berjualan buah," tutur Teguh.</p>

<p>Akibat perbuatannya, pelaku terancam Pasal 287 KUHP, lebih khusus lagi UU Nomor 35 Tahun 2014 tentang Perlindungan Anak dengan ancaman hukuman maksimal 15 tahun penjara.</p>

