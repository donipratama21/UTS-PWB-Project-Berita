<h1>Qualcomm Pilih Nama Snapdragon 888, Mengapa Bukan Snapdragon 875?</h1>
<img src="{{ asset('img/teknologi.jpg') }}" class="img-responsive" style="margin-left: auto;margin-right: auto; margin-top: auto;margin-bottom: auto;width:75%;" alt="Tri">
<p>Qualcomm resmi mengumumkan suksesor chipset Snapdragon 865 dalam acara tahunan Snapdragon Tech Summit yang digelar pekan ini. Alih-alih menggunakan nama Snapdragon 875, Qualcomm memilih nama Snapdragon 888 untuk chipset terbarunya.</p>

<p>Penamaan ini tidak sesuai tradisi Qualcomm setiap tahun. Qualcomm biasanya memberi penamaan secara berurutan untuk chipset Snapdragon seri 8 besutannya. Seperti Snapdragon 835, Snapdragon 845, hingga Snapdragon 865 yang dirilis akhir tahun 2019 lalu.</p>

<p>Dalam sesi keynote Snapdragon Tech Summit pada hari pertama, SVP & GM of Mobile, Compute and Infrastructure, Qualcomm Technologies, Alan Katouzian, mengatakan bahwa pihaknya menggunakan nomor tersebut karena ingin menawarkan beragam fitur premium.</p>

<p>"Selama lebih dari satu dekade, angka 8 selalu mengawali nama seluruh chipset kelas atas kami. Kini, kami menggunakan Snapdragon 888 karena ingin memberikan nama yang premium," ujar Alan, Selasa (1/12/2020).</p>

<p>Ia tidak menjelaskan lebih rinci korelasi antara angka 888 dan "fitur premium" yang dimaksud. Namun, apabila mengacu pada budaya China, angka 888 disebut sebagai angka pembawa keberuntungan.</p>

<p>Sebagaimana dihimpun KompasTekno dari TravelChinaGuide, Jumat (4/12/2020),  selain tentang keberuntungan, angka tersebut merupakan simbol doa dari para orang tua kepada generasi muda.</p>

<p>Angka 888 ini, bersama angka 366, 666, dan 1666 biasanya menjadi patokan bagi para orang tua untuk memasukkan uang ke dalam angpau dengan jumlah demikian. Sehingga penerima angpau mendapatkan keberuntungan.</p>

<p>Lantas, apa hubungannya dengan Qualcomm? Meski mereka merupakan perusahaan yang bermarkas di San Diego, California, Amerika Serikat, sebagian besar vendor smartphone yang digandeng diketahui memang berasal dari "Negeri Tirai Bambu".</p>

<p>Di antaranya seperti Oppo, Vivo, Xiaomi, Black Shark, ZTE, Realme, Meizu, Nubia, OnePlus dan masih banyak lagi. Beberapa vendor smartphone asal China ini juga memastikan bahwa mereka bakal meluncurkan ponsel dengan Snapdragon 888 tahun depan.</p>

<p>Sehingga, wajar saja apabila angka 888 digunakan dalam penamaan chipset Snapdragon teranyar, boleh jadi supaya SoC tersebut memberikan keberuntungan bagi Qualcomm dan para mitranya.</p>

