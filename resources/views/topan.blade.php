<h1>Topan Molave Terjang Vietnam, 2 Orang Tewas dan 26 Hilang</h1>
<img src="{{ asset('img/topan.jpeg') }}" class="img-responsive" style="margin-left: auto;margin-right: auto; margin-top: auto;margin-bottom: auto;width:75%;" alt="Tri">
<p>Hanoi - Dua orang dilaporkan tewas dan 26 orang lainnya hilang saat Topan Molave menghantam Vietnam tengah pada Rabu (28/10/2020). Topan ini menumbangkan pohon-pohon dan merusak atap rumah-rumah.
Dilansir AFP, Rabu (28/10) pihak berwenang memindahkan sekitar 375.000 orang ke tempat aman, membatalkan ratusan penerbangan dan menutup sekolah serta pantai menjelang topan, yang mendarat di selatan kota Danang dan membawa angin berkecepatan hingga 145 kilometer per jam.</p>

<p>Media pemerintah melaporkan setidaknya dua orang tewas di provinsi Quang Ngai ketika berusaha melindungi rumah mereka dari topan.</p>

<p>"Orang-orang Vietnam tangguh, namun ini adalah salah satu kerusakan terburuk yang pernah terlihat di banyak daerah," kata presiden Masyarakat Palang Merah Vietnam Nguyen Thi Xuan Thu.</p>

<p>"Badai dan banjir yang tak henti-hentinya memakan korban jiwa, semakin menghancurkan mata pencaharian dan mengisolasi jutaan orang," imbuhnya.</p>

<p>Para petugas penyelamat saat ini tengah mencari 26 nelayan yang hilang. Topan ini merupakan topan keempat di Vietnam bulan ini. Topan Molave ini telah menyebabkan listrik terputus di sejumlah wilayah.</p>

<p>Kapal angkatan laut dan pengintai dikerahkan untuk mencari awak setelah dua kapal mereka menghilang setelah mereka berusaha berlabuh, kata Wakil Perdana Menteri Trinh Dinh Dung.</p>

<p>Larangan memancing telah diberlakukan sejak Selasa (27/10) waktu setempat, sementara semua bandara di daerah itu ditutup sampai pemberitahuan lebih lanjut.</p>

<p>Topan ini menerjang setelah beberapa minggu banjir parah dan tanah longsor yang telah merenggut 130 nyawa dan merusak atau menghancurkan lebih dari 310.000 rumah, menurut Federasi Internasional Masyarakat Palang Merah dan Bulan Sabit Merah (IFRC).</p>

<p>Vietnam rentan terhadap bencana alam pada musim hujan antara bulan Juni dan November, dengan provinsi pesisir tengah biasanya terkena.</p>

